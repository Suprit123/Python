# List are used to store multiple values in a single variable.

fruits =["apple", "banana", "cherry"]
print(fruits)

# To determine how many items a list has use the len() function.
car = ["kia", "hyundai", "ford", "bmw", "audi"]
print(len(car))

# To dertermine the list type us the type() function
mix = ["abc", 12, True, 40, "male"]
print(type(mix))

# We can also use the list() constructor to make list
certifcations = list(("ccna", "ccnp", "ccie")) # Note that we need to make use of double round-brackets
print(certifcations)