# The upper() method returns the string in upper case:
a = "Hello, World!"
print(a.upper())

# The lower() method returns the string in lower case:
a = "Hello, world!"
print(a.lower())

# The strip() method removes any whitespace from the beginning or the end:
a = "Hello, World!"
print(a.strip())

# The replace() method replaces a string with another string:
a = "Hello, World!"
print(a.replace("H", "J"))

# The split() methods splits the string into the substrings if it finds instances of the separator.
a = "Hello, World!"
print(a.split(","))
